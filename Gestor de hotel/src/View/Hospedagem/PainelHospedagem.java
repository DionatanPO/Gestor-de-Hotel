        // TODO add your handling code here:
        ArrayList<String> dados = new ArrayList<>();
        dados.add(jTextFieldnome.getText());
        dados.add(numP.getSelectedItem().toString());
        dados.add(valorH.getSelectedItem().toString());
        dados.add((String) statuspg.getSelectedItem());
        dados.add(String.valueOf(txt_id.getText()));
        dados.add("Alterar");

