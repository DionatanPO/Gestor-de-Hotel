    public void addApartamento() {
        ApartamentoDao apdao = new ApartamentoDao();
        for (Apartamento ap : apdao.buscarApartamentoDisponibilidade()) {
            quartos.addItem(ap);
        }
    }

    public void criar_ler_tabela() {

        DefaultTableModel modelo = (DefaultTableModel) jTableHospedes.getModel();
        modelo.setNumRows(0);

        HospedeDao hdao = new HospedeDao();
        hdao.buscaHospedeNome(modelo);

    }

    public void criar_ler_tabela_hospedagens() {

        DefaultTableModel modelo = (DefaultTableModel) jTableHospedagens.getModel();
        modelo.setNumRows(0);

        HospedagemDao hdao = new HospedagemDao();

        hdao.tabelaRecebeDados(modelo);

    }

